"""
Operadores relacionais.
> maior, < menor, >= maior ou igual, <= menor ou igual, == igual
!= diferente.
Sempre retornarão um valor booleano, geralmente usamos com números.
"""

maior = 2 > 15
maior_igual = 2 >= 2
menor = 1 < 2
menor_igual = 1 <= 1
igual = 'a' == 'a'
diferente = 'a' != 'A'

print(maior)
print(maior_igual)
print(menor)
print(menor_igual)
print(igual)
print(diferente)