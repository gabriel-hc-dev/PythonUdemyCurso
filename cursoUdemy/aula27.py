#O sinal de + pode ser usado para adição.
#Dependendo do tipo, ele concatena valores.

#Para concatenar, podemos usar o sinal * também
#Porém, com o *, é uma repetição.

a_10x = 'A' * 10
print(a_10x)
gabriel_3x = 'Gabriel' * 3
print(gabriel_3x)