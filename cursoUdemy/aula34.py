#if, elif, else
#se, se não se, se não

entrada = input("Olá, você quer entrar ou sair do sistema? ")

if (entrada == 'entrar'):
    print("Você entrou no sistema. Bem-vindo.")
elif (entrada == 'sair'):
    print("Você saiu do sistema. Até mais!")
else:
    print("Processo não encontrado, digite 'entrar' ou 'sair'!")

print("CONFIRMADO!")